void	ft_putchar(char c);

int		main(int argc, char const *argv[])
{
	int i;

	i = 0;
	if (argc == 1)
	{
		while (argv[0][i] != '\0')
		{
			ft_putchar(argv[0][i]);
			i++;
		}
		ft_putchar('\n');
	}
	return (0);
}
