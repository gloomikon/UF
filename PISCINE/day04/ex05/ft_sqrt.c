int		ft_sqrt(int nb)
{
	int		r;
	int		sq;

	r = 1;
	sq = 0;
	if (nb == 1)
		return (1);
	while (r <= nb / 2)
	{
		sq = r * r;
		if (sq == nb)
		{
			return (r);
		}
		r = r + 1;
	}
	return (0);
}
