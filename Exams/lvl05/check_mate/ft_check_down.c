int	ft_check_down(char **board, int x, int y, int size)
{
	int i;

	i = 1;
	while (y + i <= (size - 1))
	{
		if (board[y + i][x] == 'K')
			return (1);
		if (board[y + i][x] != '.')
			break ;
		i++;
	}
	return (0);
}
