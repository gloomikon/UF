#include "chess.h"

int	ft_check_ru(char **board, int x, int y, int size)
{
	int	i;

	i = 1;
	while ((y - i) >= 0 && (x + i) <= (size - 1))
	{
		if (board[y - i][x + i] == 'K')
			return (1);
		if (board[y - i][x + i] != '.')
			break ;
		i++;
	}
	return (0);
}
