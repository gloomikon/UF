#include "chess.h"

int	ft_pawn(char **board, int x, int y, int size)
{
	if ((y - 1) >= 0 && (x - 1) >= 0)
	{
		if (board[y - 1][x - 1] == 'K')
			return (1);
	}
	if ((y - 1) >= 0 && (x + 1) <= (size - 1))
	{
		if (board[y - 1][x + 1] == 'K')
			return (1);
	}
	return (0);
}
