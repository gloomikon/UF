int	ft_check_left(char **board, int x, int y)
{
	int i;

	i = 1;
	while (x - i >= 0)
	{
		if (board[y][x - i] == 'K')
			return (1);
		if (board[y][x - i] != '.')
			break ;
		i++;
	}
	return (0);
}
