int	ft_check_right(char **board, int x, int y, int size)
{
	int i;

	i = 1;
	while (x + i <= (size - 1))
	{
		if (board[y][x + i] == 'K')
			return (1);
		if (board[y][x + i] != '.')
			break ;
		i++;
	}
	return (0);
}
