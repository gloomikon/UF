#include <string.h>

char	*ft_strpbrk(const char *s1, const char *s2)
{
	int i;
	int	j;

	i = -1;
	while (s1[++i] && (j = -1) == -1)
		while (s2[++j])
			if (s1[i] == s2[j])
				return ((char*)s1 + i);
	return (NULL);
}
